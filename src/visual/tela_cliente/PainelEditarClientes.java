    private void limparCampos(){
        campoNomeOuNomeFantasiaEditar.setText(null);
        campoEnderecoEditar.setText(null);
        campoCPFeCNPJEditar.setText(null);   
    }
    
    private boolean validarCamposEditarCliente(){
        if((campoSelecionePFouPJEditar.getSelectedIndex() != 0) && (!campoNomeOuNomeFantasiaEditar.getText().isEmpty()) && (!campoEnderecoEditar.getText().isEmpty()) && (!campoCPFeCNPJEditar.getText().isEmpty())  ){
            return true;
        }
        else{
            return false;
        }
 
    }
    
    
