    private void botaoEditarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarClientesActionPerformed

        if(validarCamposEditarCliente() == true){
            
            if(campoSelecionePFouPJEditar.getSelectedIndex() == 1){
                ClientePessoaFisicaBean clientePessoaFisicaBean = new ClientePessoaFisicaBean();
        
                clientePessoaFisicaBean.setCodigo(Dados.clienteFisicoEstaticoBean.getCodigo());
                clientePessoaFisicaBean.setNome(campoNomeOuNomeFantasiaEditar.getText().trim());
                clientePessoaFisicaBean.setEndereco(campoEnderecoEditar.getText().trim());
                clientePessoaFisicaBean.setCPF(campoCPFeCNPJEditar.getText().trim());
                clientePessoaFisicaBean.setTipo(campoSelecionePFouPJEditar.getSelectedItem().toString());
            
                facade.alterarClientePessoaFisica(clientePessoaFisicaBean, clientePessoaFisicaBean.getCodigo());
            
                PainelDeDialogoCadastrarCliente alertaDeDialogoEditarClientePF = new PainelDeDialogoCadastrarCliente(null, true);
                PainelDeDialogoCadastrarCliente.textoNomeDialogoCliente.setText("Cliente Alterado!");
                alertaDeDialogoEditarClientePF.setLocationRelativeTo(null);
                alertaDeDialogoEditarClientePF.setVisible(true);
            }
            else if(campoSelecionePFouPJEditar.getSelectedIndex() == 2){
                ClientePessoaJuridicaBean clientePessoaJuridicaBean = new ClientePessoaJuridicaBean();
        
                clientePessoaJuridicaBean.setCodigo(Dados.clienteJuridicoEstaticoBean.getCodigo());
                clientePessoaJuridicaBean.setNomeFantasia(campoNomeOuNomeFantasiaEditar.getText().trim());
                clientePessoaJuridicaBean.setEndereco(campoEnderecoEditar.getText().trim());
                clientePessoaJuridicaBean.setCNPJ(campoCPFeCNPJEditar.getText().trim());
                clientePessoaJuridicaBean.setTipo(campoSelecionePFouPJEditar.getSelectedItem().toString());
            
                facade.alterarClientePessoaJuridica(clientePessoaJuridicaBean, clientePessoaJuridicaBean.getCodigo());             
            
                PainelDeDialogoCadastrarCliente alertaDeDialogoEditarClientePJ = new PainelDeDialogoCadastrarCliente(null, true);
                PainelDeDialogoCadastrarCliente.textoNomeDialogoCliente.setText("Cliente Alterado!");
                alertaDeDialogoEditarClientePJ.setLocationRelativeTo(null);
                alertaDeDialogoEditarClientePJ.setVisible(true);
            }
            
            //limparCampos();
            
            
            clientePessoaResumoBean = new ClientePessoaResumoBean();
            clientePessoaResumoBean.setCodigo(Dados.clientePessoaResumoBean.getCodigo());
            clientePessoaResumoBean.setCliente(campoNomeOuNomeFantasiaEditar.getText().trim());
            clientePessoaResumoBean.setTipo(campoSelecionePFouPJEditar.getSelectedItem().toString());
            
            facade.alterarClienteResumo(clientePessoaResumoBean, clientePessoaResumoBean.getCodigo() );
            
            botaoCadastrarCliente.setEnabled(true);
            botaoEditarCliente.setEnabled(true);
            botaoDeletarCliente.setEnabled(true);
            
            //PainelVenderImoveis.campoSelecioneClienteDaCompra.removeAllItems();
            //PainelVenderImoveis.campoSelecioneClienteDaCompra.addItem("Selecione um Cliente");
            //PainelVenderImoveis.selecionarClientes();
            
            TelaPrincipalDoSistema.exibirPainelPesquisarCliente();
            PainelPesquisarClientes.mostrarTabelaDeClientes();
            
            
        }
        else{
           PainelDeDialogoCliente alertaDialogoEditarCliente = new PainelDeDialogoCliente(null, true);
           PainelDeDialogoCliente.textoNomeDialogoCliente.setText("Campo(s) inválido(s)!");
           alertaDialogoEditarCliente.setLocationRelativeTo(null);
           alertaDialogoEditarCliente.setVisible(true);
        }
        
        
    }//GEN-LAST:event_botaoEditarClientesActionPerformed

