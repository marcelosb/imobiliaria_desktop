    private boolean validarCamposAlugarImovel() {
        if( (!campoDiaDataDeInicio.getText().isEmpty()) && (!campoMesDataDeInicio.getText().isEmpty()) && (!campoAnoDataDeInicio.getText().isEmpty()) && (!campoDiaDataDoFim.getText().isEmpty()) && (!campoMesDataDoFim.getText().isEmpty()) && (!campoAnoDataDoFim.getText().isEmpty())
            && (campoSelecioneCorretorDoAluguel.getSelectedIndex() != 0) && (campoSelecioneImovelDoAluguel.getSelectedIndex() != 0 ) && (campoSelecioneClienteDoAluguel.getSelectedIndex() != 0) && (!campoValorNegociadoDoAluguel.getText().isEmpty()) ){
            return true;
        }else{
           return false; 
        }
    }
    
    
