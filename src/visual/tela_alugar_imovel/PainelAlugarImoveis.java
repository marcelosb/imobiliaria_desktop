    public static void selecionarCorretoresAlugar() {
        facade.listarCorretoresPeloNomeAlugar();
        for (CorretorBean corretorBean : Dados.listaDeCorretoresAlugarImovel) {
            campoSelecioneCorretorDoAluguel.addItem(corretorBean.getNome());  
        }
                
    }

    public static void selecionarImoveisDisponiveisParaAlugar() {
        facade.listarCodigoDosImoveisAlugar();
        for (ImovelBean imovelBean : Dados.listaDeImoveisAlugarImovel) {
            campoSelecioneImovelDoAluguel.addItem(String.valueOf(imovelBean.getCodigo()));          
        }
            
    }
    
    public static void selecionarClientesAlugar() {
        facade.ordenarClientesPeloNomeAlugar();
        for (ClientePessoaResumoBean clientePessoaResumoBean : Dados.listaDeClientesResumoAlugar) {
            campoSelecioneClienteDoAluguel.addItem(clientePessoaResumoBean.getCliente());          
        }
            
    }
    
