    private void campoSelecioneImovelDoAluguelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_campoSelecioneImovelDoAluguelActionPerformed

        if (campoSelecioneImovelDoAluguel.getSelectedIndex() == 0) {
            campoValorNegociadoDoAluguel.setText("R$ 0,00");
        } 
        else {
            Dados.ImovelEstaticoAlugar = campoSelecioneImovelDoAluguel.getSelectedItem().toString().trim();    
            facade.listarCodigoDosImoveisAlugar();
            for (ImovelBean imovelBean : Dados.listaDeImoveisAlugarImovel) {
                if (imovelBean.getCodigo().toString().equals(campoSelecioneImovelDoAluguel.getSelectedItem().toString().trim())) {
                    campoValorNegociadoDoAluguel.setText(imovelBean.getValorCobrado());
                }         
            }
        }
        
        campoSelecioneClienteDoAluguel.setSelectedIndex(0);
        
        
    }//GEN-LAST:event_campoSelecioneImovelDoAluguelActionPerformed

