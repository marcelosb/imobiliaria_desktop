    private void limparCamposAlugarImovel() {
        campoDiaDataDeInicio.setText(null);
        campoMesDataDeInicio.setText(null);
        campoAnoDataDeInicio.setText(null);
        campoDiaDataDoFim.setText(null);
        campoMesDataDoFim.setText(null);
        campoAnoDataDoFim.setText(null);
        campoSelecioneCorretorDoAluguel.setSelectedIndex(0);
        campoSelecioneImovelDoAluguel.setSelectedIndex(0);
        campoSelecioneClienteDoAluguel.setSelectedIndex(0);
        campoValorNegociadoDoAluguel.setText("R$ 0,00");  
    }

    @Override
    public void execute() {
        String dataDeInicioDoAluguel = campoDiaDataDeInicio.getText() + "/" + campoMesDataDeInicio.getText() + "/" + campoAnoDataDeInicio.getText();
        String dataDoFimDoAluguel = campoDiaDataDoFim.getText() + "/" + campoMesDataDoFim.getText() + "/" + campoAnoDataDoFim.getText();

        alugarImovelBean = new AlugarImovelBean();
        alugarImovelBean.setDataDeInicioDoContrato(dataDeInicioDoAluguel);
        alugarImovelBean.setDataDeFimDoContrato(dataDoFimDoAluguel);
        alugarImovelBean.setCorretor(campoSelecioneCorretorDoAluguel.getSelectedItem().toString());
        alugarImovelBean.setImovel(campoSelecioneImovelDoAluguel.getSelectedItem().toString());
        alugarImovelBean.setCliente(campoSelecioneClienteDoAluguel.getSelectedItem().toString());
        alugarImovelBean.setValorNegociado(campoValorNegociadoDoAluguel.getText());

        facade.cadastrarAluguelDeImovel(alugarImovelBean);

        PainelDeDialogoCadastrarAlugarImovel dialogoCadastrarAlugarImovel = new PainelDeDialogoCadastrarAlugarImovel(null, true);
        PainelDeDialogoCadastrarAlugarImovel.textoCadastroDialogoAlugarImovel.setText("Cadastro realizado!");
        dialogoCadastrarAlugarImovel.setLocationRelativeTo(null);
        dialogoCadastrarAlugarImovel.setVisible(true);

        facade.alterarStatusDoImovel(Integer.parseInt(campoSelecioneImovelDoAluguel.getSelectedItem().toString()));

        campoSelecioneImovelDoAluguel.removeItem(campoSelecioneImovelDoAluguel.getSelectedItem().toString());

        limparCamposAlugarImovel();

        TelaPrincipalDoSistema.exibirPainelPesquisarAlugarImovel();
        PainelPesquisarAlugarImovel.mostrarTabelaAlugarImovel();

        TelaPrincipalDoSistema.botaoEditarAlugarImovel.setVisible(true);
        TelaPrincipalDoSistema.botaoExcluirAlugarImovel.setVisible(true);
        
    }


