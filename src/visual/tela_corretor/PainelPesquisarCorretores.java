    private void personalizandoCampoUsuario(Color cor) {
        if (campoPesquisarCorretor.getText().isEmpty()) {
            campoPesquisaCorretor2.setVisible(true);
            campoPesquisaCorretor2.setBackground(cor);
            campoPesquisarCorretor.setOpaque(false);
        }
        if (!campoPesquisarCorretor.getText().isEmpty()) {
            campoPesquisarCorretor.setOpaque(true);
            campoPesquisarCorretor.setBackground(cor);
            campoPesquisaCorretor2.setVisible(false);
        }
    }
                     
    
