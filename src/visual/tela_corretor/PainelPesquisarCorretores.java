    public void popularPainelEditarCorretor() {
        if (tabelaCorretores.getSelectedRow() != -1) {
            
            Dados.corretorEstaticoBean.setRegistro(Integer.parseInt(String.valueOf(tabelaCorretores.getValueAt(tabelaCorretores.getSelectedRow(), 0))));
            
            PainelEditarCorretores.campoNomeEditarCorretor.setText((String)tabelaCorretores.getValueAt(tabelaCorretores.getSelectedRow(), 1));
            PainelEditarCorretores.campoImobiliariaVinculadaEditar.setText("IMOBILIÁRIA UEPB");
            
            Dados.corretorSelecaoVenderImoveis = tabelaCorretores.getValueAt(tabelaCorretores.getSelectedRow(), 1).toString();
            
        }

    }


