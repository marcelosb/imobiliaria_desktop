package modelo;

/**
 * @author Marcelo Henirque
 */
public class CorretorBean {
    
    private Integer registro;
    private String nome;
    private String imobiliariaVinculada;

    public Integer getRegistro() {
        return registro;
    }

    public void setRegistro(Integer registro) {
        this.registro = registro;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getImobiliariaVinculada() {
        return imobiliariaVinculada;
    }

    public void setImobiliariaVinculada(String imobiliariaVinculada) {
        this.imobiliariaVinculada = imobiliariaVinculada;
    }

    

}
